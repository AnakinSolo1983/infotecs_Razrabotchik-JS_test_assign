// Функция определяет следующий порядок сортировки
// current — текущее значение сортировки ('asc', 'desc' или null)
export function getNextSortOrder(current) {

  // Если сортировка ещё не установлена (null или undefined)
  // начинаем с возрастания
  if (!current) return 'asc';

  // Если текущая сортировка по возрастанию,
  // переключаем на убывание
  if (current === 'asc') return 'desc';

  // Если текущая сортировка по убыванию,
  // сбрасываем сортировку (возвращаем null)
  return null;
}