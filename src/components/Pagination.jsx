// Компонент Pagination отвечает за отображение кнопок страниц
// total: общее количество элементов
// limit: количество элементов на одной странице
// page: текущая активная страница
// setPage: функция для изменения текущей страницы
export default function Pagination({ total, limit, page, setPage }) {
  
  // Вычисляем общее количество страниц
  // Например: если total = 95, limit = 10 → получим 10 страниц
  const pages = Math.ceil(total / limit);

  return (
    // Контейнер с отступом сверху
    <div style={{ marginTop: '20px' }}>
      
      {/* 
        Создаём массив длиной pages 
        Array.from({ length: pages }) создаёт массив
        (_, i) — индекс текущей страницы (начинается с 0)
      */}
      {Array.from({ length: pages }, (_, i) => (
        <button
          key={i} // Уникальный ключ для React

          // Отключаем кнопку, если это текущая страница
          disabled={page === i + 1}

          // При клике устанавливаем страницу (i + 1, потому что i начинается с 0)
          onClick={() => setPage(i + 1)}
        >
          {/* Отображаем номер страницы (начиная с 1) */}
          {i + 1}
        </button>
      ))}
    </div>
  );
}